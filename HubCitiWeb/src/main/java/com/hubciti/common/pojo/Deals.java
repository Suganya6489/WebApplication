public class Deals {
	/**
	 * variable for deal ID
	 */
	private Integer dealId;
	/**
	 * variable for deal Name
	 */
	private String dealName;
	/**
	 * variable for deal Description
	 */
	private String dealDescription;
	/**
	 * variable for price
	 */
	private Double price;
	/**
	 * variable for sale Price
	 */
	private Double salePrice;
	/**
	 * variable for start Date
	 */
	private String startDate;
	/**
	 * variable for end Date
	 */
	private String endDate;
	/**
	 * variable for lower Limit
	 */
	private Integer lowerLimit;
	/**
	 * variable for deal SearchKey
	 */
	private String dealSearchKey;
	/**
	 * @return the dealId
	 */
	public Integer getDealId() {
		return dealId;
	}
	/**
	 * @param dealId the dealId to set
	 */
	public void setDealId(Integer dealId) {
		this.dealId = dealId;
	}
	/**
	 * @return the dealName
	 */
	public String getDealName() {
		return dealName;
	}
	/**
	 * @param dealName the dealName to set
	 */
	public void setDealName(String dealName) {
		this.dealName = dealName;
	}
	/**
	 * @return the dealDescription
	 */
	public String getDealDescription() {
		return dealDescription;
	}
	/**
	 * @param dealDescription the dealDescription to set
	 */
	public void setDealDescription(String dealDescription) {
		this.dealDescription = dealDescription;
	}
	/**
	 * @return the price
	 */
	public Double getPrice() {
		return price;
	}
	/**
	 * @param price the price to set
	 */
	public void setPrice(Double price) {
		this.price = price;
	}
	/**
	 * @return the salePrice
	 */
	public Double getSalePrice() {
		return salePrice;
	}
	/**
	 * @param salePrice the salePrice to set
	 */
	public void setSalePrice(Double salePrice) {
		this.salePrice = salePrice;
	}
	/**
	 * @return the startDate
	 */
	public String getStartDate() {
		return startDate;
	}
	/**
	 * @param startDate the startDate to set
	 */
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	/**
	 * @return the endDate
	 */
	public String getEndDate() {
		return endDate;
	}
	/**
	 * @param endDate the endDate to set
	 */
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	/**
	 * @return the lowerLimit
	 */
	public Integer getLowerLimit() {
		return lowerLimit;
	}
	/**
	 * @param lowerLimit the lowerLimit to set
	 */
	public void setLowerLimit(Integer lowerLimit) {
		this.lowerLimit = lowerLimit;
	}
	/**
	 * @return the dealSearchKey
	 */
	public String getDealSearchKey() {
		return dealSearchKey;
	}
	/**
	 * @param dealSearchKey the dealSearchKey to set
	 */
	public void setDealSearchKey(String dealSearchKey) {
		this.dealSearchKey = dealSearchKey;
	}
	
	
	

}
